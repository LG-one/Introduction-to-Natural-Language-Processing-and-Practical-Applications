def lesson_rule_based_ner():
    """
    课程 4.3：基于规则的 NER

    ━━━━━━━ 生活类比 ━━━━━━━
    基于规则的 NER 就像查电话簿：
    - 你知道所有姓氏（规则）
    - 你有完整的人名/地名/机构名词典
    - 遇到名字，先查词典，再用规则判断
    """
    print_separator("4.3 基于规则的 NER")

    print("""
    基于规则的 NER 方法：

    1. 词典匹配：
       - 维护人名、地名、机构名的词典
       - 遇到词典中的词直接识别

    2. 规则匹配：
       - 姓氏 + 名字 → 人名
       - 地名后缀（省、市、县）→ 地名
       - 机构后缀（大学、公司、集团）→ 机构名

    3. 最长匹配优先：
       - "北京大学" 应该匹配为一个机构名
       - 而不是拆成 "北京"（地名）+ "大学"（普通词）
    """)

    # 实际演示
    test_sentences = [
        "小明在北京大学学习自然语言处理",
        "马云创办了阿里巴巴公司",
        "张三和李四去了上海旅游",
        "清华大学位于北京",
    ]

    for sent in test_sentences:
        print(f"\n  句子: {sent}")
        entities = rule_based_ner(sent)
        if entities:
            for ent_text, ent_type, start, end in entities:
                print(f"    [{ent_text}] → {ent_type}")
        else:
            print("    未识别到实体")


