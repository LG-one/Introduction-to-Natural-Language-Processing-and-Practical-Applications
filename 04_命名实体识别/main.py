def lesson_comparison():
    """课程 4.7：三种 NER 方法对比"""
    print_separator("4.7 三种 NER 方法对比")

    print("""
    ┌────────────┬──────────────────────────────────────────────────────┐
    │  方法      │  特点                                                │
    ├────────────┼──────────────────────────────────────────────────────┤
    │  规则方法  │  简单直接，依赖词典和规则，准确率高但召回率低          │
    │  HMM      │  用概率推断，能处理未知词，但特征有限                  │
    │  CRF      │  特征丰富，效果最好，但需要训练数据                    │
    │  jieba    │  开箱即用，适合快速原型，但精度有限                    │
    └────────────┴──────────────────────────────────────────────────────┘

    选择建议：
    - 数据少、要求高 → 规则方法 + 词典
    - 数据多、要求高 → CRF 或深度学习
    - 快速原型 → jieba 或规则方法
    - 学习理解 → HMM（概念清晰，便于理解）
    """)

    # 实际对比
    sentence = "小明在北京大学学习自然语言处理"
    print(f"  测试句子: {sentence}\n")

    # 规则方法
    print("  1. 规则方法:")
    entities = rule_based_ner(sentence)
    for ent_text, ent_type, _, _ in entities:
        print(f"     [{ent_text}] → {ent_type}")

    # HMM 方法
    print("\n  2. HMM 方法:")
    hmm_model = HMMNERModel()
    entities = hmm_model.extract_entities(sentence)
    for ent_text, ent_type in entities:
        print(f"     [{ent_text}] → {ent_type}")

    # CRF 方法
    print("\n  3. CRF 方法:")
    crf_model = SimpleCRFNER()
    entities = crf_model.extract_entities(sentence)
    for ent_text, ent_type in entities:
        print(f"     [{ent_text}] → {ent_type}")

    # jieba 方法
    print("\n  4. jieba 方法:")
    entities = demo_jieba_ner(sentence)
    for ent_text, ent_type in entities:
        print(f"     [{ent_text}] → {ent_type}")


