def lesson_hmm_ner():
    """
    课程 4.4：HMM 命名实体识别

    ━━━━━━━ 生活类比 ━━━━━━━
    HMM NER 就像猜谜语：
    - 你看到一个字（观测值），猜它属于哪种实体（隐藏状态）
    - 而且前面的猜测会影响后面的猜测
    - 例如：如果"张"被标记为 B-PER，那"三"很可能是 I-PER
    """
    print_separator("4.4 HMM 命名实体识别")

    print("""
    HMM NER 的核心思想：

    把 NER 看作序列标注问题：
    - 观测序列：句子中的每个字
    - 隐藏状态：每个字的 BIO 标签
    - 目标：找到最可能的 BIO 标签序列

    三个概率：
    1. 初始概率：句子第一个字的 BIO 标签概率
    2. 转移概率：BIO 标签之间的转移规律
       例如：B-PER 后面很可能是 I-PER（人名通常不止一个字）
    3. 发射概率：某个 BIO 标签下出现某个字的概率
       例如：B-PER 下出现"张"的概率很高（很多姓张的人）

    解码算法：维特比算法
    - 找到概率最大的 BIO 标签序列
    """)

    # 实际演示
    model = HMMNERModel()

    test_sentences = [
        "小明在北京大学学习",
        "马云创办了阿里巴巴",
        "张三去上海旅游",
        "清华大学是中国最好的大学",
    ]

    for sent in test_sentences:
        print(f"\n  句子: {sent}")

        # 显示 BIO 标注
        bio_result = model.predict(sent)
        bio_str = " ".join([f"{ch}/{tag}" for ch, tag in bio_result])
        print(f"    BIO: {bio_str}")

        # 提取实体
        entities = model.extract_entities(sent)
        if entities:
            for ent_text, ent_type in entities:
                print(f"    [{ent_text}] → {ent_type}")


