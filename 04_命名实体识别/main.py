def lesson_crf_ner():
    """
    课程 4.5：CRF 命名实体识别

    ━━━━━━━ 生活类比 ━━━━━━━
    CRF 就像法官判案：
    - 不仅看当前证据（当前字）
    - 还要看前后关联的证据（上下文）
    - 综合所有证据做出最合理的判断

    CRF 比 HMM 更强大，因为它可以使用更丰富的特征！
    """
    print_separator("4.5 CRF 命名实体识别")

    print("""
    CRF（条件随机场）vs HMM：

    ┌────────────┬──────────────────────────────────────────┐
    │            │  HMM              │  CRF                 │
    ├────────────┼───────────────────┼──────────────────────┤
    │  建模对象  │  联合概率 P(X,Y)  │  条件概率 P(Y|X)     │
    │  假设      │  观测值之间独立    │  无独立性假设        │
    │  特征      │  仅用当前字        │  可用任意特征        │
    │  训练      │  EM 算法           │  最大似然估计        │
    └────────────┴───────────────────┴──────────────────────┘

    CRF 的特征函数示例：
    - 当前字是否在人名词典中 → 更可能是 B-PER
    - 前一个字标签是 B-PER → 当前字更可能是 I-PER
    - 当前字是姓氏 → 更可能是 B-PER
    - 当前字在地名词典中 → 更可能是 B-LOC
    """)

    # 实际演示
    model = SimpleCRFNER()

    test_sentences = [
        "小明在北京大学学习",
        "马云创办了阿里巴巴",
        "张三去上海旅游",
        "清华大学是中国最好的大学",
    ]

    for sent in test_sentences:
        print(f"\n  句子: {sent}")

        # 显示 BIO 标注
        bio_result = model.predict(sent)
        bio_str = " ".join([f"{ch}/{tag}" for ch, tag in bio_result])
        print(f"    BIO: {bio_str}")

        # 提取实体
        entities = model.extract_entities(sent)
        if entities:
            for ent_text, ent_type in entities:
                print(f"    [{ent_text}] → {ent_type}")


