def lesson_bio_tagging():
    """
    课程 4.2：BIO 标注体系

    ━━━━━━━ 生活类比 ━━━━━━━
    BIO 就像给排队的人发号码牌：
    - B（Begin）：排头，举着"XX队"的牌子
    - I（Inside）：队伍中间的人
    - O（Outside）：不属于任何队伍的路人
    """
    print_separator("4.2 BIO 标注体系")

    print("""
    NER 通常使用 BIO 标注体系来标记每个字/词：

      B = Begin（实体的开始）
      I = Inside（实体的内部）
      O = Outside（不属于任何实体）

    例子："小明在北京大学学习"

      ┌──────┬──────┬──────┬──────┬──────┬──────┬──────┬──────┐
      │  小  │  明  │  在  │  北  │  京  │  大  │  学  │ 学习 │
      ├──────┼──────┼──────┼──────┼──────┼──────┼──────┼──────┤
      │B-PER │I-PER │  O   │B-LOC │I-LOC │I-LOC │I-LOC │  O   │
      └──────┴──────┴──────┴──────┴──────┴──────┴──────┴──────┘

    规则：
    1. 实体的第一个字用 B-xxx 标记
    2. 实体的后续字用 I-xxx 标记
    3. 不属于实体的字用 O 标记
    4. B-xxx 后面只能跟 I-xxx 或 O，不能跟 B-yyy（不同类型实体不能相连）
    """)

    # 实际演示
    sentence = "小明在北京大学学习"
    print(f"  句子: {sentence}")
    entities = rule_based_ner(sentence)
    bio_result = sentence_to_bio(sentence, entities)
    print(f"  BIO 标注:")
    for char, tag in bio_result:
        print(f"    {char} → {tag}")


