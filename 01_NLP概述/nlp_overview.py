# ==============================================================================
# 演示 1：字符串是 NLP 的基础
# ==============================================================================
#
# 在 NLP 中，我们处理的最基本的数据类型就是"字符串"（str）。
# 所有的文本、句子、段落，在 Python 中都是字符串。
#
# 就像乐高积木是最基本的拼装单元，字符串是 NLP 的基本单元。
#
# ==============================================================================

def demo_string_basics():
    """演示字符串的基本操作"""

    print("=" * 60)
    print("演示 1：字符串 — NLP 的基本单元")
    print("=" * 60)

    # ---------------------------------------------------------------
    # 创建字符串
    # ---------------------------------------------------------------
    # 字符串就是一串字符，用引号括起来
    # 就像把一串珠子用线穿起来

    text = "我喜欢自然语言处理"
    print(f"\n原始文本: {text}")

    # ---------------------------------------------------------------
    # 字符串的长度
    # ---------------------------------------------------------------
    # len() 函数可以计算字符串有多少个字符
    # 就像数一串珠子有多少颗

    print(f"文本长度: {len(text)} 个字符")

    # ---------------------------------------------------------------
    # 访问单个字符
    # ---------------------------------------------------------------
    # 用索引（下标）可以访问字符串中的某个字符
    # Python 的索引从 0 开始
    # 就像排队时，第一个人的编号是 0（不是 1）

    print(f"第一个字: {text[0]}")   # 我
    print(f"第三个字: {text[2]}")   # 自
    print(f"最后一个字: {text[-1]}")  # 理（-1 表示从末尾数第一个）

    # ---------------------------------------------------------------
    # 字符串切片
    # ---------------------------------------------------------------
    # 切片可以截取字符串的一部分
    # 语法：text[开始:结束]（包含开始，不包含结束）
    # 就像从一串珠子中取几颗出来

    print(f"前四个字: {text[0:4]}")   # 我喜欢自
    print(f"第五个字起: {text[4:]}")   # 然语言处理

    # ---------------------------------------------------------------
    # 字符串拼接
    # ---------------------------------------------------------------
    # 用 + 号可以把两个字符串拼在一起
    # 就像把两串珠子接在一起

    greeting = "你好！" + text
    print(f"拼接后: {greeting}")

    # ---------------------------------------------------------------
    # 字符串分割
    # ---------------------------------------------------------------
    # split() 可以按指定的分隔符把字符串切成列表
    # 这是 NLP 中非常常用的操作！

    sentence = "我 喜欢 自然 语言 处理"
    words = sentence.split(" ")  # 按空格分割
    print(f"\n句子: {sentence}")
    print(f"分割后: {words}")
    print(f"词数: {len(words)} 个")

    # ---------------------------------------------------------------
    # 字符串查找
    # ---------------------------------------------------------------
    # in 关键字可以判断一个字符串是否包含另一个字符串
    # 就像在一篇文章中搜索某个关键词

    keyword = "语言"
    if keyword in text:
        print(f"\n'{text}' 中包含关键词 '{keyword}'")


# ==============================================================================
# 演示 2：中文和英文的区别
# ==============================================================================
#
# 中文和英文在 NLP 处理上有很大区别：
#   - 英文：单词之间有空格，天然分词
#   - 中文：字与字之间没有空格，需要分词
#
# 这就像：
#   - 英文是"乐高积木已经分好类，放在不同的盒子里"
#   - 中文是"所有积木混在一个大箱子里，需要自己分类"
#
# ==============================================================================

def demo_chinese_vs_english():
    """演示中文和英文在 NLP 处理上的区别"""

    print("\n" + "=" * 60)
    print("演示 2：中文 vs 英文 — NLP 处理的区别")
    print("=" * 60)

    # ---------------------------------------------------------------
    # 英文：天然分词
    # ---------------------------------------------------------------
    # 英文单词之间有空格，可以直接用 split() 分词
    # 就像已经切好的蛋糕，直接吃就行

    english = "I love natural language processing"
    eng_words = english.split(" ")
    print(f"\n英文原文: {english}")
    print(f"英文分词: {eng_words}")
    print(f"词数: {len(eng_words)}")

    # ---------------------------------------------------------------
    # 中文：没有天然分隔符
    # ---------------------------------------------------------------
    # 中文字与字之间没有空格
    # 如果直接用 split()，只能按字分割，不能按词分割
    # 就像一整块蛋糕，需要自己切

    chinese = "我喜欢自然语言处理"
    print(f"\n中文原文: {chinese}")
    print(f"按字分割: {list(chinese)}")
    print(f"字数: {len(chinese)}")

    # 但是！按字分割不等于按词分割
    # 正确的分词应该是：我 / 喜欢 / 自然语言处理
    # 或者更细的：我 / 喜欢 / 自然 / 语言 / 处理

    print(f"\n正确的分词应该是:")
    print(f"  粗粒度: ['我', '喜欢', '自然语言处理']")
    print(f"  细粒度: ['我', '喜欢', '自然', '语言', '处理']")

    print("\n这就是为什么中文 NLP 需要专门的分词技术！")
    print("（下一章我们会详细学习中文分词）")


# ==============================================================================
# 演示 3：文本预处理的基本步骤
# ==============================================================================
#
# 在做 NLP 任务之前，通常需要对原始文本进行预处理。
# 就像做菜之前要洗菜、切菜一样。
#
# 预处理的常见步骤：
#   1. 去除多余空白
#   2. 统一大小写（英文）
#   3. 去除特殊字符
#   4. 分词
#   5. 去除停用词（的、了、是 等无意义的词）
#
# ==============================================================================

def demo_text_preprocessing():
    """演示文本预处理的基本步骤"""

    print("\n" + "=" * 60)
    print("演示 3：文本预处理 — 做菜前的准备工作")
    print("=" * 60)

    # 原始文本（模拟从网页抓取的脏数据）
    raw_text = "  今天天气  真好！！！  我们 去 公园 玩 吧...  "
    print(f"\n原始文本: '{raw_text}'")

    # ---------------------------------------------------------------
    # 第一步：去除多余空白
    # ---------------------------------------------------------------
    # strip() 去除首尾空格
    # replace() 替换指定字符
    # 就像洗菜时去掉泥沙

    text = raw_text.strip()              # 去除首尾空格
    text = text.replace("  ", " ")       # 把两个空格变成一个
    print(f"去空白后: '{text}'")

    # ---------------------------------------------------------------
    # 第二步：去除特殊字符
    # ---------------------------------------------------------------
    # 用正则表达式（后面章节会详细学）去除不需要的字符
    # 就像切菜时去掉烂叶子

    import re
    text = re.sub(r'[！。.…]+', '', text)  # 去除标点
    print(f"去标点后: '{text}'")

    # ---------------------------------------------------------------
    # 第三步：分词
    # ---------------------------------------------------------------
    # 简单起见，这里按空格分词
    # 实际项目中会用 jieba 等专业分词工具

    words = text.split(" ")
    print(f"分词结果: {words}")

    # ---------------------------------------------------------------
    # 第四步：去除停用词
    # ---------------------------------------------------------------
    # 停用词就是高频但无意义的词，如"的"、"了"、"是"
    # 就像做菜时把不能吃的皮、核去掉

    stopwords = {"我们", "去", "吧", "的", "了", "是"}
    filtered_words = [w for w in words if w not in stopwords]
    print(f"去停用词: {filtered_words}")

    print("\n预处理完成！现在可以进行后续的 NLP 分析了。")


# ==============================================================================
# 演示 4：NLP 任务的输入和输出
# ==============================================================================
#
# 不同的 NLP 任务，输入和输出是不同的。
# 就像不同的机器，放进去的东西和出来的东西不一样。
#
# ==============================================================================

def demo_nlp_tasks():
    """演示各种 NLP 任务的输入输出"""

    print("\n" + "=" * 60)
    print("演示 4：NLP 任务的输入和输出")
    print("=" * 60)

    sentence = "小明在北京大学学习自然语言处理"
    print(f"\n输入句子: {sentence}")

    # ---------------------------------------------------------------
    # 任务 1：分词（Tokenization）
    # ---------------------------------------------------------------
    # 输入：一个句子
    # 输出：一个词列表

    tokens = ["小明", "在", "北京大学", "学习", "自然语言处理"]
    print(f"\n【分词】输入: 句子")
    print(f"        输出: {tokens}")

    # ---------------------------------------------------------------
    # 任务 2：词性标注（POS Tagging）
    # ---------------------------------------------------------------
    # 输入：一个词列表
    # 输出：每个词对应的词性

    pos_tags = [
        ("小明", "人名"),
        ("在", "介词"),
        ("北京大学", "机构名"),
        ("学习", "动词"),
        ("自然语言处理", "学科名"),
    ]
    print(f"\n【词性标注】输入: 词列表")
    print(f"            输出: {pos_tags}")

    # ---------------------------------------------------------------
    # 任务 3：命名实体识别（NER）
    # ---------------------------------------------------------------
    # 输入：一个句子
    # 输出：识别出的实体及其类型

    entities = [
        ("小明", "PERSON"),
        ("北京大学", "ORGANIZATION"),
    ]
    print(f"\n【命名实体识别】输入: 句子")
    print(f"                输出: {entities}")

    # ---------------------------------------------------------------
    # 任务 4：情感分析（Sentiment Analysis）
    # ---------------------------------------------------------------
    # 输入：一段文本
    # 输出：情感类别和置信度

    print(f"\n【情感分析】输入: '这部电影太好看了！'")
    print(f"          输出: 正面情感 (置信度: 95%)")

    print(f"\n【情感分析】输入: '服务态度很差，再也不来了。'")
    print(f"          输出: 负面情感 (置信度: 92%)")

    # ---------------------------------------------------------------
    # 任务 5：文本分类（Text Classification）
    # ---------------------------------------------------------------
    # 输入：一段文本
    # 输出：文本所属的类别

    print(f"\n【文本分类】输入: '湖人队今天赢了比赛，詹姆斯砍下30分'")
    print(f"          输出: 类别 = 体育")

    print(f"\n【文本分类】输入: '央行宣布降息，股市大涨'")
    print(f"          输出: 类别 = 财经")


