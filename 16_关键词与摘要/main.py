def lesson_keyword_extraction():
    """第一部分：关键词提取"""

    print_separator("16.1 关键词提取")

    print("""
    ┌─────────────────────────────────────────────────────────┐
    │                 关键词提取 = 自动"划重点"                 │
    │                                                         │
    │   输入：一篇文章                                         │
    │   输出：最重要的 N 个词                                   │
    │                                                         │
    │   两种主要方法：                                         │
    │   ┌─────────────┬──────────────────────────────────┐    │
    │   │ TF-IDF      │ 基于词频和文档频率                │    │
    │   │ TextRank    │ 基于图的排序算法（PageRank 思想） │    │
    │   └─────────────┴──────────────────────────────────┘    │
    └─────────────────────────────────────────────────────────┘
    """)

    # 示例文档
    doc1 = "机器学习是人工智能的重要分支。深度学习是机器学习的一个热门方向。神经网络是深度学习的核心技术。"
    doc2 = "自然语言处理让计算机能够理解人类语言。分词是中文自然语言处理的基础任务。"
    doc3 = "今天股市大涨，上证指数突破三千点。投资者对经济前景持乐观态度。"

    documents = [doc1, doc2, doc3]

    print("  示例文档:")
    for i, doc in enumerate(documents):
        print(f"    文档 {i}: {doc[:40]}...")

    # TF-IDF 关键词
    print("\n  [方法 1] TF-IDF 关键词提取:")
    for i, doc in enumerate(documents):
        keywords = extract_keywords_tfidf(doc, documents, top_k=3)
        kw_str = ", ".join([f"{w}({s:.3f})" for w, s in keywords])
        print(f"    文档 {i}: {kw_str}")

    # TextRank 关键词
    print("\n  [方法 2] TextRank 关键词提取:")
    for i, doc in enumerate(documents):
        keywords = extract_keywords_textrank(doc, top_k=3)
        kw_str = ", ".join([f"{w}({s:.3f})" for w, s in keywords])
        print(f"    文档 {i}: {kw_str}")


def lesson_text_summarization():
    """第二部分：文本摘要"""

    print_separator("16.2 文本摘要")

    print("""
    ┌─────────────────────────────────────────────────────────┐
    │                 文本摘要 = 自动"写读书笔记"               │
    │                                                         │
    │   两种方式：                                             │
    │   ┌─────────────┬──────────────────────────────────┐    │
    │   │ 抽取式摘要  │ 从原文中"摘"出重要的句子          │    │
    │   │ 生成式摘要  │ 用自己的话"写"出摘要              │    │
    │   └─────────────┴──────────────────────────────────┘    │
    │                                                         │
    │   本章重点：抽取式摘要                                   │
    │                                                         │
    │   步骤：                                                │
    │   1. 把文章分成句子                                     │
    │   2. 给每个句子打分（关键词、位置、长度）                │
    │   3. 选出得分最高的几个句子                              │
    │   4. 按原文顺序排列，就是摘要                            │
    └─────────────────────────────────────────────────────────┘
    """)

    # 长文本示例
    article = (
        "自然语言处理是人工智能的重要方向。它让计算机能够理解和生成人类语言。"
        "近年来，深度学习技术推动了自然语言处理的快速发展。"
        "预训练语言模型如 BERT 和 GPT 取得了突破性进展。"
        "这些模型在文本分类、情感分析、机器翻译等任务上表现出色。"
        "未来，自然语言处理将在更多领域发挥重要作用。"
    )

    documents = [article]

    print(f"  原文:\n    {article}\n")

    # TF-IDF 摘要
    print("  [方法 1] TF-IDF 抽取式摘要:")
    summary = extract_summary(article, documents, top_k=2)
    for sent in summary:
        print(f"    - {sent}")

    # TextRank 摘要
    print("\n  [方法 2] TextRank 抽取式摘要:")
    summary = textrank_summary(article, top_k=2)
    for sent in summary:
        print(f"    - {sent}")


def lesson_comparison():
    """第三部分：方法对比"""

    print_separator("16.3 方法对比")

    print("""
    ┌─────────────────────────────────────────────────────────┐
    │              TF-IDF vs TextRank 对比                     │
    ├──────────────┬──────────────────┬────────────────────────┤
    │              │ TF-IDF           │ TextRank               │
    ├──────────────┼──────────────────┼────────────────────────┤
    │ 原理         │ 词频 × 文档频率   │ 图排序（PageRank）     │
    │ 优点         │ 简单、快速        │ 不需要外部语料库       │
    │ 缺点         │ 需要多篇文档      │ 计算量较大             │
    │ 适用场景     │ 多文档关键词提取  │ 单文档关键词提取       │
    └──────────────┴──────────────────┴────────────────────────┘

    实际应用建议：
    1. 如果有多篇文档 → TF-IDF 效果更好
    2. 如果只有单篇文档 → TextRank 更合适
    3. 可以两者结合使用，取交集
    """)


