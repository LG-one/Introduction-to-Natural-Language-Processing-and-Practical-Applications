# ==============================================================================
# 第一部分：编辑距离（Levenshtein Distance）
# ==============================================================================
#
# 编辑距离是衡量两个字符串"差异程度"的经典方法。
#
# 核心问题：把字符串 A 变成字符串 B，最少需要多少步操作？
# 操作有三种：
#   1. 插入一个字符
#   2. 删除一个字符
#   3. 替换一个字符
#
# 生活类比：
#   想象你在玩一个"变身游戏" ——
#   "kitten" 要变成 "sitting"，最少需要几步？
#
#     kitten → sitten  （把 k 替换成 s）     第1步
#     sitten → sittin  （把 e 替换成 i）     第2步
#     sittin → sitting  （在末尾插入 g）     第3步
#
#   编辑距离 = 3
#
# ==============================================================================


def levenshtein_distance(s1: str, s2: str) -> int:
    """
    计算两个字符串的编辑距离（Levenshtein Distance）

    ━━━━━━━ 生活类比 ━━━━━━━
    想象你在教一个小朋友写字：
    - "kitten" 和 "sitting" 差多少？
    - 你需要告诉小朋友：改几个字、加几个字、删几个字
    - 编辑距离就是"最少需要教几次"

    ━━━━━━━ 算法原理 ━━━━━━━
    使用动态规划（Dynamic Programming）：
    - dp[i][j] = s1 的前 i 个字符变成 s2 的前 j 个字符需要的最少操作数
    - 三种操作取最小值：
      a. dp[i-1][j] + 1    （删除 s1[i]）
      b. dp[i][j-1] + 1    （插入 s2[j]）
      c. dp[i-1][j-1] + cost  （替换，cost=0 如果相同，cost=1 如果不同）

    参数：
        s1: 第一个字符串
        s2: 第二个字符串

    返回：
        编辑距离（非负整数）

    示例：
        >>> levenshtein_distance("kitten", "sitting")
        3
        >>> levenshtein_distance("你好", "你好")
        0
    """
    # 获取两个字符串的长度
    m, n = len(s1), len(s2)

    # 创建动态规划表格
    # dp[i][j] 表示 s1 的前 i 个字符变成 s2 的前 j 个字符需要的最少操作数
    # 表格大小为 (m+1) x (n+1)，多出的一行/列用于处理空字符串的情况
    dp = [[0] * (n + 1) for _ in range(m + 1)]

    # 初始化第一列：s1 的前 i 个字符变成空字符串，需要删除 i 次
    for i in range(m + 1):
        dp[i][0] = i

    # 初始化第一行：空字符串变成 s2 的前 j 个字符，需要插入 j 次
    for j in range(n + 1):
        dp[0][j] = j

    # 填充动态规划表格
    # 从 (1,1) 开始，逐行逐列计算
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            # 如果当前字符相同，不需要操作（cost=0）
            # 如果不同，需要替换一次（cost=1）
            cost = 0 if s1[i - 1] == s2[j - 1] else 1

            # 三种操作取最小值：
            #   1. dp[i-1][j] + 1   → 删除操作
            #   2. dp[i][j-1] + 1   → 插入操作
            #   3. dp[i-1][j-1] + cost → 替换操作（字符相同时 cost=0）
            dp[i][j] = min(
                dp[i - 1][j] + 1,        # 删除
                dp[i][j - 1] + 1,        # 插入
                dp[i - 1][j - 1] + cost   # 替换
            )

    # 右下角的值就是最终的编辑距离
    return dp[m][n]


def levenshtein_similarity(s1: str, s2: str) -> float:
    """
    基于编辑距离计算相似度（归一化到 0~1）

    ━━━━━━━ 生活类比 ━━━━━━━
    编辑距离告诉你"差多少步"，但步数本身不好比较。
    比如"差 3 步"，如果原词有 100 个字符，那很相似；
    如果原词只有 3 个字符，那就完全不同了！

    所以我们把它归一化到 0~1：
    - 相似度 = 1 - 编辑距离 / max(len(s1), len(s2))
    - 1 表示完全相同，0 表示完全不同

    参数：
        s1: 第一个字符串
        s2: 第二个字符串

    返回：
        相似度分数（0.0 到 1.0）

    示例：
        >>> levenshtein_similarity("kitten", "sitting")
        0.5714...  （大约 57% 相似）
    """
    # 如果两个字符串都为空，视为完全相同
    if not s1 and not s2:
        return 1.0

    # 计算编辑距离
    distance = levenshtein_distance(s1, s2)

    # 归一化：1 - 距离 / 最大长度
    max_len = max(len(s1), len(s2))
    return 1.0 - distance / max_len


# ==============================================================================
# 第二部分：余弦相似度（Cosine Similarity）
# ==============================================================================
#
# 余弦相似度是衡量两个向量"方向相似性"的方法。
#
# 核心思想：
#   把文本转换成向量（一组数字）
#   然后计算两个向量夹角的余弦值
#   夹角越小，余弦值越接近 1，表示越相似
#
# 生活类比：
#   想象两个人各拿一个指南针：
#   - 如果都指向北方，夹角为 0°，cos(0°) = 1 → 完全一致
#   - 如果一个指北一个指南，夹角为 180°，cos(180°) = -1 → 完全相反
#   - 如果一个指东一个指北，夹角为 90°，cos(90°) = 0 → 无关
#
# 在文本分析中：
#   我们把文本变成一个"词频向量"
#   比如 "我喜欢吃苹果" → [1, 1, 1, 1, 1]
#   然后用余弦公式计算两个向量的相似度
#
# ==============================================================================


def cosine_similarity_manual(vec1: list, vec2: list) -> float:
    """
    手动计算两个向量的余弦相似度（不依赖 numpy）

    ━━━━━━━ 公式 ━━━━━━━
                        vec1 . vec2
    cosine_sim = ─────────────────────────
                  ||vec1|| * ||vec2||

    其中：
    - vec1 . vec2 = 点积 = sum(a[i] * b[i])
    - ||vec1|| = 向量长度 = sqrt(sum(a[i]^2))

    ━━━━━━━ 数值例子 ━━━━━━━
    vec1 = [1, 2, 3], vec2 = [4, 5, 6]

    点积 = 1*4 + 2*5 + 3*6 = 4 + 10 + 18 = 32
    ||vec1|| = sqrt(1 + 4 + 9) = sqrt(14) ≈ 3.74
    ||vec2|| = sqrt(16 + 25 + 36) = sqrt(77) ≈ 8.77
    余弦相似度 = 32 / (3.74 * 8.77) ≈ 0.9746

    参数：
        vec1: 第一个向量（数字列表）
        vec2: 第二个向量（数字列表）

    返回：
        余弦相似度（-1 到 1 之间）

    示例：
        >>> cosine_similarity_manual([1, 0], [1, 0])
        1.0
        >>> cosine_similarity_manual([1, 0], [0, 1])
        0.0
    """
    # 检查向量长度是否相同
    if len(vec1) != len(vec2):
        raise ValueError(f"向量维度不一致: {len(vec1)} vs {len(vec2)}")

    # 计算点积（dot product）
    # 点积 = 对应位置元素相乘后求和
    dot_product = sum(a * b for a, b in zip(vec1, vec2))

    # 计算两个向量的长度（模）
    # ||vec|| = sqrt(sum(x^2))
    norm1 = sum(a * a for a in vec1) ** 0.5
    norm2 = sum(b * b for b in vec2) ** 0.5

    # 避免除以零
    if norm1 == 0 or norm2 == 0:
        return 0.0

    # 余弦相似度 = 点积 / (长度1 * 长度2)
    return dot_product / (norm1 * norm2)


def text_to_vector(text: str) -> dict:
    """
    将文本转换为词频向量（Bag of Words）

    ━━━━━━━ 生活类比 ━━━━━━━
    想象你是一个仓库管理员：
    - 每个词就是一种货物
    - 词频就是这种货物的数量

    "我喜欢吃苹果，苹果很好吃"
    → {"我": 1, "喜欢": 1, "吃": 2, "苹果": 2, "很": 1, "好": 1}

    这样就把一段文字变成了一个"货物清单"（向量）。

    参数：
        text: 输入文本

    返回：
        词频字典 {词: 频率}
    """
    # 简单的按字符分词（对于中文，每个字就是一个"词"）
    # 在实际项目中，应该使用 jieba 等分词工具
    vector = {}
    for char in text:
        # 跳过空白字符和标点
        if char.strip() and char not in "，。！？、；：""''（）【】《》":
            vector[char] = vector.get(char, 0) + 1
    return vector


def cosine_similarity_text(text1: str, text2: str) -> float:
    """
    计算两段文本的余弦相似度

    ━━━━━━━ 步骤 ━━━━━━━
    1. 将两段文本分别转换为词频向量
    2. 合并两个向量的关键词，得到统一的维度
    3. 计算余弦相似度

    参数：
        text1: 第一段文本
        text2: 第二段文本

    返回：
        余弦相似度（0 到 1 之间）

    示例：
        >>> cosine_similarity_text("今天天气好", "今天天气不错")
        0.75 左右
    """
    # 第一步：将文本转换为词频向量
    vec1 = text_to_vector(text1)
    vec2 = text_to_vector(text2)

    # 第二步：合并所有关键词，形成统一的维度
    # 比如 vec1 有"今"、"天"，vec2 有"今"、"气"
    # 合并后维度为 ["今", "天", "气"]
    all_words = set(vec1.keys()) | set(vec2.keys())

    # 第三步：将两个向量转换为等长的数字列表
    list1 = [vec1.get(word, 0) for word in all_words]
    list2 = [vec2.get(word, 0) for word in all_words]

    # 第四步：计算余弦相似度
    return cosine_similarity_manual(list1, list2)


# ==============================================================================
# 第三部分：Jaccard 相似度
# ==============================================================================
#
# Jaccard 相似度是最简单的相似度计算方法之一。
#
# 核心思想：
#   把两个文本看作"集合"
#   相似度 = 交集大小 / 并集大小
#
# 生活类比：
#   想象两个人的购物清单：
#   - 小明的清单：{苹果, 香蕉, 牛奶, 面包}
#   - 小红的清单：{苹果, 面包, 鸡蛋, 水}
#
#   交集（都买的）：{苹果, 面包} → 2 种
#   并集（总共提到的）：{苹果, 香蕉, 牛奶, 面包, 鸡蛋, 水} → 6 种
#   Jaccard 相似度 = 2 / 6 = 0.33
#
#   → 小明和小红的购物习惯有 33% 相似
#
# ==============================================================================


def jaccard_similarity(set1: set, set2: set) -> float:
    """
    计算两个集合的 Jaccard 相似度

    ━━━━━━━ 公式 ━━━━━━━
                        |A ∩ B|
    Jaccard(A, B) = ─────────────
                        |A ∪ B|

    其中：
    - A ∩ B = 交集（两个集合共有的元素）
    - A ∪ B = 并集（两个集合所有元素）
    - |S| = 集合的大小（元素个数）

    ━━━━━━━ 例子 ━━━━━━━
    A = {1, 2, 3}, B = {2, 3, 4}
    交集 = {2, 3} → 2 个
    并集 = {1, 2, 3, 4} → 4 个
    Jaccard = 2/4 = 0.5

    参数：
        set1: 第一个集合
        set2: 第二个集合

    返回：
        Jaccard 相似度（0 到 1 之间）

    示例：
        >>> jaccard_similarity({1, 2, 3}, {2, 3, 4})
        0.5
    """
    # 如果两个集合都为空，视为完全相同
    if not set1 and not set2:
        return 1.0

    # 计算交集大小
    intersection = len(set1 & set2)

    # 计算并集大小
    union = len(set1 | set2)

    # 避免除以零
    if union == 0:
        return 0.0

    return intersection / union


def jaccard_similarity_text(text1: str, text2: str, use_words: bool = False) -> float:
    """
    计算两段文本的 Jaccard 相似度

    ━━━━━━━ 生活类比 ━━━━━━━
    就像比较两篇文章"用了多少相同的词"。
    如果两篇文章用了很多相同的词，说明它们可能在讨论相似的话题。

    参数：
        text1: 第一段文本
        text2: 第二段文本
        use_words: 是否按词分词（True）还是按字符分词（False）

    返回：
        Jaccard 相似度（0 到 1 之间）
    """
    if use_words:
        # 按词分词：用空格分割（适用于英文或已分词的中文）
        set1 = set(text1.split())
        set2 = set(text2.split())
    else:
        # 按字符分词：每个字符是一个元素（适用于未分词的中文）
        # 跳过空白和标点
        set1 = set(c for c in text1 if c.strip() and c not in "，。！？、；：""''（）【】《》")
        set2 = set(c for c in text2 if c.strip() and c not in "，。！？、；：""''（）【】《》")

    return jaccard_similarity(set1, set2)


# ==============================================================================
# 第四部分：模糊搜索
# ==============================================================================
#
# 模糊搜索就是"差不多就行"的搜索方式。
#
# 生活类比：
#   你在手机通讯录里找"张三"，但你不记得确切的名字。
#   你输入"张三"，手机可能会提示：
#   - 张三（完全匹配）
#   - 张三丰（多一个字）
#   - 张山（少一个字）
#   - 张三四（改一个字）
#
#   这就是模糊搜索 —— 找到"最接近"的结果。
#
# ==============================================================================


def fuzzy_search(query: str, candidates: list, threshold: float = 0.5) -> list:
    """
    模糊搜索：在候选列表中找到与查询最相似的文本

    ━━━━━━━ 生活类比 ━━━━━━━
    就像在一堆名片中找"最像"的那张：
    - 名字完全一样 → 得分 1.0
    - 名字很接近 → 得分 0.8
    - 名字完全不同 → 得分 0.1

    只有得分超过 threshold 的才会被返回。

    参数：
        query: 查询文本
        candidates: 候选文本列表
        threshold: 相似度阈值（0 到 1），低于此值的不返回

    返回：
        列表，每个元素是 (候选文本, 相似度)，按相似度降序排列
    """
    results = []

    for candidate in candidates:
        # 使用编辑距离相似度
        sim = levenshtein_similarity(query, candidate)

        # 只保留相似度超过阈值的
        if sim >= threshold:
            results.append((candidate, sim))

    # 按相似度降序排列（最相似的排在前面）
    results.sort(key=lambda x: x[1], reverse=True)

    return results


def fuzzy_search_advanced(query: str, candidates: list, top_k: int = 3) -> list:
    """
    高级模糊搜索：综合多种相似度指标

    ━━━━━━━ 生活类比 ━━━━━━━
    就像找对象，不能只看一个方面：
    - 外貌相似？（编辑距离）
    - 性格相似？（Jaccard）
    - 综合评分！（加权平均）

    参数：
        query: 查询文本
        candidates: 候选文本列表
        top_k: 返回前 k 个结果

    返回：
        列表，每个元素是 (候选文本, 综合得分)
    """
    results = []

    for candidate in candidates:
        # 编辑距离相似度（关注字符串的整体相似性）
        edit_sim = levenshtein_similarity(query, candidate)

        # Jaccard 相似度（关注字符集合的重叠程度）
        jaccard_sim = jaccard_similarity_text(query, candidate)

        # 余弦相似度（关注字符频率的分布相似性）
        cosine_sim = cosine_similarity_text(query, candidate)

        # 综合得分：加权平均
        # 编辑距离权重最高，因为它最能反映整体相似性
        combined = edit_sim * 0.4 + jaccard_sim * 0.3 + cosine_sim * 0.3

        results.append((candidate, combined))

    # 按综合得分降序排列
    results.sort(key=lambda x: x[1], reverse=True)

    # 返回前 top_k 个
    return results[:top_k]


# ==============================================================================
# 第五部分：评估工具
# ==============================================================================


def compare_all_methods(text1: str, text2: str) -> dict:
    """
    用所有方法比较两个文本的相似度

    参数：
        text1: 第一段文本
        text2: 第二段文本

    返回：
        包含各种相似度指标的字典
    """
    return {
        "编辑距离": levenshtein_distance(text1, text2),
        "编辑距离相似度": round(levenshtein_similarity(text1, text2), 4),
        "Jaccard相似度": round(jaccard_similarity_text(text1, text2), 4),
        "余弦相似度": round(cosine_similarity_text(text1, text2), 4),
    }


